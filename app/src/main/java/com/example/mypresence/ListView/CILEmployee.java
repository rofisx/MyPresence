package com.example.mypresence.ListView;

public class CILEmployee {
    private String namecil, comecil;

    public CILEmployee(String namecil, String comecil) {
        this.namecil = namecil;
        this.comecil = comecil;
    }

    public String getNamecil() {
        return namecil;
    }

    public void setNamecil(String namecil) {
        this.namecil = namecil;
    }

    public String getComecil() {
        return comecil;
    }

    public void setComecil(String comecil) {
        this.comecil = comecil;
    }
}
