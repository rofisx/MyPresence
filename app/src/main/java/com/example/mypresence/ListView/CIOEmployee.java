package com.example.mypresence.ListView;

public class CIOEmployee {
    private String namecio, comecio;

    public CIOEmployee(String namecio, String comecio) {
        this.namecio = namecio;
        this.comecio = comecio;
    }

    public String getNamecio() {
        return namecio;
    }

    public void setNamecio(String namecio) {
        this.namecio = namecio;
    }

    public String getComecio() {
        return comecio;
    }

    public void setComecio(String comecio) {
        this.comecio = comecio;
    }
}
